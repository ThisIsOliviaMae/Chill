package Classes;

public class Scholarship {
	private int scholarship_id;
	private String scholarship_name;
	private String scholarship_description;
	private String scholarship_school;
	private String scholarship_website;
	public Scholarship(){
		
	}
	public int getScholarship_id() {
		return scholarship_id;
	}
	public void setScholarship_id(int scholarship_id) {
		this.scholarship_id = scholarship_id;
	}
	public String getScholarship_name() {
		return scholarship_name;
	}
	public void setScholarship_name(String scholarship_name) {
		this.scholarship_name = scholarship_name;
	}
	public String getScholarship_description() {
		return scholarship_description;
	}
	public void setScholarship_description(String scholarship_description) {
		this.scholarship_description = scholarship_description;
	}
	public String getScholarship_school() {
		return scholarship_school;
	}
	public void setScholarship_school(String scholarship_school) {
		this.scholarship_school = scholarship_school;
	}
	public String getScholarship_website() {
		return scholarship_website;
	}
	public void setScholarship_website(String scholarship_website) {
		this.scholarship_website = scholarship_website;
	}
}
