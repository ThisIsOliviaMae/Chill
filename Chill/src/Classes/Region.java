package Classes;

public class Region {
	private int region_id;
	private String region_designation;
	private String region_name;
	
	public Region()
	{
		
	}

	public int getRegion_id() {
		return region_id;
	}

	public void setRegion_id(int region_id) {
		this.region_id = region_id;
	}

	public String getRegion_designation() {
		return region_designation;
	}

	public void setRegion_designation(String region_designation) {
		this.region_designation = region_designation;
	}

	public String getRegion_name() {
		return region_name;
	}

	public void setRegion_name(String name) {
		this.region_name = name;
	}
}
